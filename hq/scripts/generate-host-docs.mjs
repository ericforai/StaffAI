#!/usr/bin/env node

import fs from 'fs';
import path from 'path';

const scriptDir = path.dirname(new URL(import.meta.url).pathname);
const hqDir = path.resolve(scriptDir, '..');
const manifestPath = path.join(hqDir, 'config', 'host-manifest.json');
const generatedDir = path.join(hqDir, 'generated');

const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf-8'));

function mkdirp(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function writeFile(filePath, content) {
  mkdirp(path.dirname(filePath));
  fs.writeFileSync(filePath, content, 'utf-8');
}

function renderHostDoc(host) {
  const snippetFile =
    host.id === 'claude'
      ? 'CLAUDE-snippet.md'
      : host.id === 'codex'
        ? 'AGENTS-snippet.md'
        : 'GEMINI-snippet.md';

  const snippet = `## ${host.instructionTitle}

Use The Agency HQ as the primary multi-agent command deck for expert discovery, hiring, and discussion orchestration.

- Web UI: ${manifest.project.webUrl}
- API: ${manifest.project.apiUrl}
- MCP entry: \`${manifest.project.mcpEntry}\`
- State dir: \`${manifest.project.stateDir}\`

Preferred operating model:
1. Use the Web UI to search, hire, and compose expert squads.
2. Use the MCP tools when you need the same capabilities inside the host client.
3. If a capability is unavailable in this host, degrade to the Web UI instead of inventing a parallel workflow.

Degrade strategy for ${host.label}:
- ${host.degradeStrategy}
`;

  const guide = `# ${host.label} Integration

This file is generated from \`hq/config/host-manifest.json\`. Do not hand-edit.

## Summary

- Host: ${host.label}
- Config file: \`${host.configFile}\`
- Snippet target: ${host.snippetTarget}
- Capability level: ${host.capabilityLevel}
- State dir: \`${manifest.project.stateDir}\`

## Recommended Injection Snippet

${snippet}

## MCP Example

\`\`\`json
{
  "command": "node",
  "args": ["${path.resolve(hqDir, 'backend/dist/mcp-server.js')}"]
}
\`\`\`

## Notes

- Generated docs are the source of truth for host onboarding.
- If the host cannot support the full local workflow, fall back to the Web UI.
`;

  return { guide, snippet, snippetFile };
}

mkdirp(generatedDir);

for (const host of manifest.hosts) {
  const hostDir = path.join(generatedDir, host.id);
  const { guide, snippet, snippetFile } = renderHostDoc(host);
  writeFile(path.join(hostDir, 'README.md'), guide);
  writeFile(path.join(hostDir, snippetFile), snippet);
}

const readme = `# Generated Host Docs

This directory is generated by \`hq/scripts/generate-host-docs.mjs\`.

Hosts generated:
${manifest.hosts.map((host) => `- ${host.label} -> \`generated/${host.id}/\``).join('\n')}

Use these files as the canonical onboarding snippets for each local client.
`;

writeFile(path.join(generatedDir, 'README.md'), readme);

console.log(`Generated host docs for ${manifest.hosts.length} hosts in ${generatedDir}`);
